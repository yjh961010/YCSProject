package com.example.neoheulge.dto;

public class AcountDTO {
	private String acount_id;
	private String acount_number;
	private String member_id;
	private int money;
	private String acount_status;
	
	public String getAcount_id() {
		return acount_id;
	}
	public void setAcount_id(String acount_id) {
		this.acount_id = acount_id;
	}
	public String getAcount_number() {
		return acount_number;
	}
	public void setAcount_number(String acount_number) {
		this.acount_number = acount_number;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public String getAcount_status() {
		return acount_status;
	}
	public void setAcount_status(String acount_status) {
		this.acount_status = acount_status;
	}
	
	
}
